<?php

$num1 = 100;
$num2 = 2;
$operacao = '/'; 

switch ($operacao) {
    case '+':
        echo $num1 + $num2;
        break;
    case '-':
        echo $num1 - $num2;
        break;
    case '*':
        echo $num1 * $num2;
        break;
    case '/':
        echo $num1 / $num2;
        break;
}

?>